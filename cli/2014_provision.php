foreach ($roles as $obj) {
    $user = $DB->get_record('user', array('username' => $obj[0]));

    if (!isset($user) || !$user) {
        $user = new \stdClass();
        $user->username = \core_text::convert($obj[0], 'utf-8', 'utf-8');
        $user->email = $user->username . "@kent.ac.uk";
        $user->auth = "kentsaml";
        $user->password = "not cached";
        $user->confirmed = 1;
        $user->mnethostid = $CFG->mnet_localhost_id;
        $user->firstname = substr($user->username, 0, 1);
        $user->lastname = substr($user->username, 1, 2);

        $user->id = user_create_user($user, false);
    }

    $category = $DB->get_record('course_categories', array(
        'id' => $obj[2],
        'name' => $obj[1]
    ));
    $role = $DB->get_record('role', array('shortname' => $obj[3]));

    if (!$user || !$category || !$role) {
        echo 'We dont have everything we need! ' . $obj[0] . ' ' . $obj[1] . ' ' . $obj[3] . "\n";
        continue;
    }

    $context = \context_coursecat::instance($category->id);
    if (!user_has_role_assignment($user->id, $role->id, $context->id)) {
        role_assign($role->id, $user->id, $context->id);
    }
}